



RADIUS = 10
import math



 
 
 
 
 

def calculate_circle_area(radius):
    """Supplied with the radius, calculate the area of a circle."""
    area = math.pi * radius ** 2
    return area


 
  
 
 
 
circle_area = calculate_circle_area(RADIUS)
print(circle_area)
                                                                          

